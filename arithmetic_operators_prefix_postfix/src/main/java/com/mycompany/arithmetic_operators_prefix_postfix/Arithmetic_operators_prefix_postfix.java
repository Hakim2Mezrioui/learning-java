package com.mycompany.arithmetic_operators_prefix_postfix;

/**
 *
 * @author H-black
 */
public class Arithmetic_operators_prefix_postfix {

    public static void main(String[] args) {
        int x = 1;
        System.out.println("x++ " + x++);
        System.out.println("x = " + x);
        System.out.println("++x " + ++x);
        System.out.println("x = " + x);
        System.out.println("x-- " + x--);
        System.out.println("x = " + x);
        System.out.println("--x " + --x);
        System.out.println("x = " + x);
        
        
    }
}
