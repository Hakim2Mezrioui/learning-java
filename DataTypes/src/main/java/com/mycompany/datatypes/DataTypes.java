package com.mycompany.datatypes;

/**
 *
 * @author H-black
 */
public class DataTypes {

    public static void main(String[] args) {
        System.out.println(Byte.MAX_VALUE);
        byte byteNumber = -128;  // -128 à 127
        System.out.println(byteNumber);
        
        System.out.println(Short.MAX_VALUE);
        short shortNumber = 32588; // -32768 à 32767
        System.out.println(shortNumber);
        
        System.out.println(Integer.MAX_VALUE);
        int intergerNumber = 2147483647;
        System.out.println(intergerNumber);
        
        System.out.println(Long.MAX_VALUE);
        long longNumber = 9223372036854775807L;
        System.out.println(longNumber);
        
        System.out.println(Float.MAX_VALUE);
        float floatNumber = 3.4028235E38f;
        System.out.println(floatNumber);
        
        System.out.println(Double.MAX_VALUE);
        double doubleNumber = 1.7976931348623157E308;
        System.out.println(doubleNumber);
        
        char caractere = 'h';
        System.out.println(caractere);
        
        String myName = "mezrioui hakim";
        System.out.println(myName);
        
        System.out.println(Long.MIN_VALUE);
    }
}
